// add the item 
function addItem(listIndex) {
 let newItemName = $("#addItem").val();
 let newItemObj = {
     name: newItemName,
     checked: false,
     category: "",
     
 };
 Department[listIndex].listItems.push(newItemObj);
 loadListItems(listIndex);
}
//  console.log(LISTS);
// }

function deleteItem(listIndex, idx) {
    Department[listIndex].listItems.splice(idx,1);
    loadListItems(listIndex);
    
   
};



// Loads in the items in the list
function loadListItems(listIndex){
    let listString = `<button onclick="loadLists()">Back</button><ul>`;
$.each(Department[listIndex].listItems, function (idx, listItem,) {
    listString += `<li id="${idx}" class="{listItem.checked ? "strike": ""}"><input ${listItem.checked ? checked = "checked": ""} type="checkbox" id="${idx}" name="${listItem.name}" onclick="itemChecked(this, ${listIndex}, ${idx})">
    <span>${listItem.name}</span> 
    <span class="delete" onclick="deleteItem(${listIndex}, ${idx})">Delete</span>
    </li>`; 
});
    
    
    listString += `</ul>
   <div class=addItemInput">
  <input id="addItem" type="text">
     <button onclick="addItem(${listIndex})">Add Item</button></div>`;
    $("#app").html(listString);
}

// // loads in the lists 
function loadLists() {
    let listString = "<ul>";
// //  Beginning list tag first   
    $.each(Department, function (idx, list) {
        listString += `<li id="${idx}" onclick="loadListItems(${idx})">${list.name} <span class="right">Items: ${list.listItems.length}</span></li>
        `;
    });
// //  loops through each List    
    listString += "</ul>";
    $("#app").html(listString);
}

