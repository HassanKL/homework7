function itemChecked(element, listIndex, itemIndex ) {

    $(element).parent().toggleClass("strike");
    let checkedValue = !Department[listIndex].listItems[itemIndex].checked;
    // toggles true or false
    Department[listIndex].listItems[itemIndex].checked = checkedValue
    // // stores the value 
    console.log("checked ", Department);
    }

function loadData () {
    $.each(Department, function (idx, list ){
        $("#app").append(`<div id="${idx}" <div class="shoppingContainer">
        <h2>${list.name}</h2>
        <div class="shoppingImage">
          <img src="images/${list.img}" alt="${list.name}" />
        </div>
        <div class="itemDescription">
        ${list.itemDescription}
        </div>
      </div>` );
    });
initListeners();
}


function initListeners() {
    $(".shoppingContainer").click(function(e) { let itemIndex = e.currentTarget.id;
      
        $("#app").html(`<div class="game">
        <button onclick="loadLists()">Load List</button>
        <h2>${Department[itemIndex].name}</h2>
        <div class="gameImg">
          <img src="images/${Department[itemIndex].img}" alt="canoe" />
        </div>
        <div class="gameDes">
        ${Department[itemIndex].itemDescription}
        </div>
        <div class="close">CLOSE</div></div>`);

        addCloseListener();
    });
}

    function addCloseListener(){
        $(".close").click(function(){
            $("#app").html("");
            loadData();
        })
    }

    $(document).ready(function () {
       
        loadData();
    });

